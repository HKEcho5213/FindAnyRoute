func Sensitive_Information(fileContent string) []string {
	//信息收集
	for i := len(nucleiRegex) - 1; i >= 0; i-- {
		// 对应的正则表达式匹配
		tmpResult := nucleiRegex[i].FindAllString(fileContent, -1)
		if tmpResult != nil {
			// 将匹配的结果添加到结果集
			m_route_lists := sliceToMap(Informationlist) // 将切片转换为 map
			for _, match := range tmpResult {
				// 去除两侧的引号
				info := strings.Trim(match, `" '`)
				fmt.Println("发现敏感信息：" + info)
				if _, exists := m_route_lists[info]; exists { //判断切片中是否已经存在路径，决定是否存入切片
					fmt.Println("敏感信息：" + info + "已经存在于结果中")
				} else {
					Informationlist = append(Informationlist, info)
				}
			}
		}
	}
	return Informationlist
}
